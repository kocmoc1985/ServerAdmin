    public static void updateClientCount(int clients) {
        if(clients > 0){
            updateStatus("active");
        }else{
            updateStatus("listening");
        }
        //
        jLabel8.setText("" + clients);
    }

    public static void showMessage(String msg) {
        jTextArea1.append(get_proper_date_time_same_format_on_all_computers() + "   " + msg + "\n");
    }

    public static String get_proper_date_time_same_format_on_all_computers() {
        DateFormat formatter = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
        Calendar calendar = Calendar.getInstance();
        return formatter.format(calendar.getTime());
    }

