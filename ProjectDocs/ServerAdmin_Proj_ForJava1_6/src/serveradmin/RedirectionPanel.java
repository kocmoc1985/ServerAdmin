    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        //
        if (PORT_REDIRECT != null) {
            return;
        }
        //
        int srcPort = Integer.parseInt(jTextField1.getText());
        String destHost = jTextField2.getText();
        int destPort = Integer.parseInt(jTextField3.getText());
        //
        PORT_REDIRECT = new MyPortRedirection(srcPort, destHost, destPort);
        //
        Thread x = new Thread(PORT_REDIRECT);
        x.start();
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (PORT_REDIRECT == null) {
            return;
        }
        //
        String destHost = jTextField5.getText();
        int destPort = Integer.parseInt(jTextField6.getText());
        //
        PORT_REDIRECT.setDestHost(destHost);
        PORT_REDIRECT.setDestPort(destPort);
        //
        showMessage("Next client connecting to port:" + jTextField1.getText() + " will be redirected to " + destHost + ": " + destPort);
    }//GEN-LAST:event_jButton3ActionPerformed

